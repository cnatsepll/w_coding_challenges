// a design pattern is a reusable solution that can be applied to occuring problems in sofware design (JS Applications)
// Can also be though of as programming templates
// Situations can vary significantly

// Types of JS patterns
// Module - used for overall structure, break the code up into modules with private elements
// Revealing Module Pattern - variation of the above pattern
// Singleton - similar to module but we can only create one instance of an object
// Factory - used to create many many objects
// Observer - used to subscribe and unsubscribe to and from events
// Mediator - central mediator and colleuges that branch off from it
// State - create and manage the state of the application and its content